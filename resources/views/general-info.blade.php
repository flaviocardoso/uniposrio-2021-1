<p><strong>4. DO USO DOS RESULTADOS</strong></p>

<p><strong>4.1 Cada Programa far&aacute; uso dos resultados do exame escrito e da entrevista para a sele&ccedil;&atilde;o e classifica&ccedil;&atilde;o dos candidatos SEGUNDO SEUS PR&Oacute;PRIOS CRIT&Eacute;RIOS. A aceita&ccedil;&atilde;o ou a n&atilde;o-aceita&ccedil;&atilde;o em um Programa n&atilde;o implica a aceita&ccedil;&atilde;o ou n&atilde;o-aceita&ccedil;&atilde;o em outro Programa.</strong></p>

<p><strong>5. DO REAPROVEITAMENTO DE NOTAS</strong></p>

<p><strong>5.1 Todos os programas aceitar&atilde;o o reaproveitamento de notas de provas anteriores do UNIPOSRIO ou do EUF. </strong></p>

<p><strong>A nota de edi&ccedil;&otilde;es passadas do UNIPOSRIO e EUF ser&aacute; normalizada de acordo com a seguinte m&eacute;todo:</strong></p>

<p><strong>NR&nbsp;= (SR/SA) * (NA &ndash; MNA) + MNR</strong></p>

<p><strong>Onde NR&nbsp;&eacute; a nota recalculada, NA&nbsp;&eacute; a nota obtida anteriormente pelo(a) candidato(a), MNA&nbsp;&eacute; a m&eacute;dia do exame anterior do(a) candidato(a) , SA&nbsp;&eacute; o desvio padr&atilde;o da exame anteriormente realizado pelo(a) candidato(a), MNR&nbsp;&eacute; a m&eacute;dia do exame em curso e SR&nbsp;&eacute; o desvio padr&atilde;o do exame em curso.</strong></p>

<p><strong>6. DO RESULTADO FINAL</strong></p>

<p><strong>6.1 Os Programas de P&oacute;s-Gradua&ccedil;&atilde;o que realizam o exame divulgaram na p&aacute;gina do UNIPOSRIO, em conjunto, os resultados do processo seletivo a partir do dia 20 de Dezembro de 2020.</strong></p>

