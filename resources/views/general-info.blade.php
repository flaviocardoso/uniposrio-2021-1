<p><strong>3.4 To sign up for the Interview, follow these steps:</strong></p>

<p>&nbsp;</p>

<p><strong>3.4.1 The applicant must pre-register at the UNIPOSRIO system by accessing the page and the INSCRI&Ccedil;&Otilde;ES / CADASTRE-SE menu, creating a login and a password.</strong></p>

<p><strong>3.4.2 After completing and SAVING the requested data, the applicant will receive an email to confirm their pre-registration. By clicking on the link provided by email, the candidate confirms the pre-registration and can now have access to the REGISTRATIONS.</strong></p>

<p><strong>3.4.3 The applicant must log in again in the system, now through the INSCRI&Ccedil;&Otilde;ES menu, using his login and password created and defined in the previous steps.</strong></p>

<p><strong>3.4.4 The candidate must complete his data and complete his registration by attaching the required documentation through the SUBMETER ARQUIVOS menu.</strong></p>

<p><strong>3.4.5 The candidate should ask for TWO letters or recommendations, to be filled online. In the case of a master student applying to Ph.D., the letter from his(her) master&#39;s supervisor is mandatory.</strong></p>

<p><strong>3.5 The Applicant must submit the following documents through the website at the time of registration:</strong></p>

