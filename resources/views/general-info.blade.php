<p><strong>5. USING OLD EXAM SCORES</strong></p>

<p>&nbsp;</p>

<p><strong>5.1 All programs will accept the reuse of previous test scores from UNIPOSRIO or EUF. </strong></p>

<pre>
<span style="font-family:arial,helvetica,sans-serif"><strong>The grade of past editions of UNIPOSRIO will be normalized according to the following method:</strong></span></pre>

<p><strong>NR&nbsp;= (SR/SA) * (NA &ndash; MNA) + MNR</strong></p>

<pre>
<span style="font-family:arial,helvetica,sans-serif"><strong>Where NR is the recalculated grade, NA is the grade obtained previously by the candidate, MNA is the average of the previous exam of the candidate, SA is the standard deviation of the exam previously performed by the ) candidate, MNR is the average of the current exam and SR is the standard deviation of the current exam.</strong></span></pre>

